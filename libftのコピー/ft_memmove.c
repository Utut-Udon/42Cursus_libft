#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*c_dst;
	char	*c_src;

	c_dst = (char *)dst;
	c_src = (char *)src;
	if (!dst && !src)
		return (NULL);
	if (c_dst > c_src)
	{
		while (len-- > 0)
			c_dst[len] = c_src[len];
	}
	else
	{
		while (len-- > 0)
		{
			*c_dst = *c_src;
			c_dst++;
			c_src++;
		}
	}
	return (dst);
}
